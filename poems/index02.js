// 여기서부터 popup windows
function toggleText01() {
    var moreText = document.getElementById("moreText01");
  
    if (moreText.style.display === "none") {
      moreText.style.display = "inline"; // 내용을 보이게 함
    } else {
      moreText.style.display = "none"; // 내용을 숨김
    }
}

